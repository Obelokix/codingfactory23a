package gr.aueb.cf.ch2;

/**
 * Demonstrates the Math class.
 */
public class MathApp {
    public static void main(String[] args) {
        System.out.println("The absolute value of -5 is: " + Math.abs(-5));
        System.out.println("The min between 1, 10 is: " + Math.min(1, 10));
        System.out.println("The max between 1, 10 is: " + Math.max(1, 10));
    }
}
